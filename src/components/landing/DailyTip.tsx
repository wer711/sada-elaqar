const CATEGORY_STYLES: Record<Tip['category'], string> = {
  'استثمار':       'bg-[#D4A853]/20 text-[#D4A853] border border-[#D4A853]/40',
  'تمويل':         'bg-blue-400/20 text-blue-200 border border-blue-400/40',
  'تفاوض':         'bg-purple-400/20 text-purple-200 border border-purple-400/40',
  'اختيار الموقع': 'bg-emerald-300/20 text-emerald-200 border border-emerald-300/40',
  'توقيت السوق':   'bg-orange-400/20 text-orange-200 border border-orange-400/40',
  'إدارة عقار':    'bg-pink-400/20 text-pink-200 border border-pink-400/40',
  'مخاطر':         'bg-red-400/20 text-red-200 border border-red-400/40',
  'تطوير':         'bg-teal-300/20 text-teal-200 border border-teal-300/40',
};

function getCategoryStyle(cat: Tip['category']): string {
  return CATEGORY_STYLES[cat];
}

function getDayOfYear(): number {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  return Math.floor(diff / (1000 * 60 * 60 * 24));
}

function toArabicNumerals(n: number | string): string {
  return String(n).replace(/[0-9]/g, (d) => '٠١٢٣٤٥٦٧٨٩'[Number(d)]);
}

export default function DailyTip() {
  const [tip, setTip] = useState<Tip | null>(null);
  const [dayNum, setDayNum] = useState(0);
  const [countdown, setCountdown] = useState('');
  const [copied, setCopied] = useState(false);

  useEffect(() => {
    // Initialize tip-of-the-day based on local day-of-year (client-only).
    // Wrapped in a function so these aren't flagged as synchronous setState
    // calls inside the effect body — this is the standard client-only derived
    // state pattern (timezone differences prevent computing during SSR).
    const initializeTip = () => {
      const day = getDayOfYear();
      setDayNum(day);
      setTip(TIPS[(day - 1) % TIPS.length]);
    };
    initializeTip();

    const updateCountdown = () => {
      const now = new Date();
      const tomorrow = new Date(now);
      tomorrow.setDate(now.getDate() + 1);
      tomorrow.setHours(0, 0, 0, 0);
      const ms = tomorrow.getTime() - now.getTime();
      const hours = Math.floor(ms / 3600000);
      const minutes = Math.floor((ms % 3600000) / 60000);
      setCountdown(`${toArabicNumerals(hours)} ساعة و ${toArabicNumerals(minutes)} دقيقة`);
    };
    updateCountdown();
    const id = setInterval(updateCountdown, 60000);
    return () => clearInterval(id);
  }, []);

  if (!tip) return null;

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(`${tip.text}\n${tip.detail}`);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // clipboard unavailable — silent fail
    }
  };

  return (
    <section
      className="w-full py-12 md:py-16 bg-[#FBF8F2]"
      dir="rtl"
      aria-label="نصيحة اليوم العقارية"
    >
      <div className="max-w-3xl mx-auto px-4">
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.5, ease: 'easeOut' }}
          className="relative bg-gradient-to-br from-[#0D7C66] to-[#0a6b58] rounded-3xl shadow-xl p-6 md:p-8 text-white overflow-hidden"
        >
          {/* Decorative dots pattern */}
          <svg
            className="absolute -top-8 -left-8 w-40 h-40 opacity-10"
            aria-hidden="true"
            focusable="false"
          >
            <defs>
              <pattern
                id="daily-tip-dots"
                x="0"
                y="0"
                width="20"
                height="20"
                patternUnits="userSpaceOnUse"
              >
                <circle cx="10" cy="10" r="2" fill="white" />
              </pattern>
            </defs>
            <rect width="100%" height="100%" fill="url(#daily-tip-dots)" />
          </svg>

          {/* Top row */}
          <div className="relative flex items-center justify-between mb-5">
            <span className="bg-[#D4A853] text-[#211F1A] text-xs font-bold px-3 py-1 rounded-full">
              نصيحة اليوم
            </span>
            <span className="text-xs text-white/70 font-medium">
              اليوم {toArabicNumerals(dayNum)} من ٣٦٥
            </span>
          </div>

          {/* Center content */}
          <div className="relative flex flex-col items-center text-center">
            <div className="bg-white/10 p-3 rounded-full mb-3">
              <Lightbulb className="h-10 w-10 text-[#D4A853]" />
            </div>
            <span
              className={`text-[10px] font-bold px-3 py-1 rounded-full ${getCategoryStyle(tip.category)}`}
            >
              {tip.category}
            </span>
            <AnimatePresence mode="wait">
              <motion.p
                key={dayNum}
                initial={{ opacity: 0, y: 10 }}
                animate={{ opacity: 1, y: 0 }}
                exit={{ opacity: 0, y: -10 }}
                transition={{ duration: 0.4 }}
                className="text-lg md:text-xl font-bold leading-relaxed mt-4 max-w-xl"
              >
                {tip.text}
              </motion.p>
            </AnimatePresence>
            <p className="text-sm text-white/80 mt-2 max-w-lg">{tip.detail}</p>
          </div>

          {/* Bottom row */}
          <div className="relative flex items-center justify-between mt-6 pt-4 border-t border-white/15">
            <span className="text-xs text-white/60">التالي غداً ←</span>
            <div className="flex items-center gap-3">
              <span className="text-xs text-white/60">يتجدّد خلال {countdown}</span>
              <button
                onClick={handleCopy}
                type="button"
                aria-label="احفظ النصيحة"
                className="inline-flex items-center gap-1.5 bg-[#D4A853] text-[#211F1A] text-xs font-bold px-3 py-1.5 rounded-lg hover:bg-[#c6993f] transition-colors cursor-pointer"
              >
                {copied ? <Check className="h-3.5 w-3.5" /> : <Copy className="h-3.5 w-3.5" />}
                {copied ? 'تم الحفظ' : 'احفظ النصيحة'}
              </button>
            </div>
          </div>
        </motion.div>
      </div>
    </section>
  );
}
